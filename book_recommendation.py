"""# Rete Bayiesiana (Naive Bayes)"""

#Ricerca della struttura migliore

hc = HillClimbSearch(train)
best_model_structure = hc.estimate(scoring_method=BIC(train))
print("Archi della struttura ottimale:", best_model_structure.edges())

#Creazione del modello sulla base della struttura trovata al passo precedente

bayesian_network_model = DiscreteBayesianNetwork(best_model_structure.edges())

#Addestramento del modello

bayesian_network_model.fit(train, estimator=BayesianEstimator, prior_type="BDeu")

#Test del modello

#Inizializzazione dell'oggetto per l'inferenza
inference = VariableElimination(bayesian_network_model)

# Raccolta della lista dei nodi che fanno parte della rete
network_nodes = bayesian_network_model.nodes()

#Raccolta della lista degli stati che la rete conosce per ogni nodo
state_names = bayesian_network_model.get_cpds('liked').state_names

#Definizione di una funzione per effettuare la predizione su una riga
def predict_book(row):
    evidence = {}
    for node in bayesian_network_model.nodes():
        if node != 'liked':
            valore_test = row[node]
            # Si ottiene la lista dei valori che la rete conosce per questo specifico nodo
            stati_conosciuti = bayesian_network_model.get_cpds(node).state_names[node]

            #Se il valore nel test non è tra quelli conosciuti, non lo si passa come evidence (la rete userà la probabilità a priori)
            if valore_test in stati_conosciuti:
                evidence[node] = valore_test
            else:
                pass
    if not evidence:
        return 'unknown'
    try:
        prediction = inference.map_query(variables=['liked'], evidence=evidence, show_progress=False)
        return str(prediction['liked'])
    except Exception:
        return 'error'

#Esecuzione della funzione sul test set
test['prediction'] = test.apply(predict_book, axis=1)

#Pulizia delle stringhe prima della valutazione
test['liked'] = test['liked'].astype(str).str.lower().str.strip()
test['prediction'] = test['prediction'].astype(str).str.lower().str.strip()

#Stampa del report di classificazione
print("\nREPORT DI CLASSIFICAZIONE - RETE BAYESIANA")
print(classification_report(test['liked'], test['prediction']))

# Generazione della matrice di confusione per la Rete Bayesiana
cm_bn = confusion_matrix(test['liked'], test['prediction'])

# Visualizzazione della matrice di confusione
plt.figure(figsize=(8, 6))
