plt.show()

"""# Logica Proposizionale"""

#Creazione della Knowledge Base (Estrazione dei fatti)

#Definizione della funzione per rendere i testi compatibili con Prolog (trasforma in minuscolo, toglie spazi e caratteri non alfanumerici)
def clean_for_prolog(text):
    if pd.isna(text): return "unknown"
    clean = "".join(e for e in str(text).lower() if e.isalnum() or e == '_')
    return clean if clean else "unknown"

# Generazione del file .pl contenete la knowledge base
with open('conoscenza_libri.pl', 'w') as f:
    for _, row in df.iterrows():
        # Estrazione dei dati dal dataframe pulito
        titolo = clean_for_prolog(row['title'])
        autore = clean_for_prolog(row['author'])
        g1 = clean_for_prolog(row['genere_1'])
        g2 = clean_for_prolog(row['genere_2'])
        g3 = clean_for_prolog(row['genere_3'])
        pagine = row['page_count']
        rapporto = row['voters/rating']
        liked = row['liked']

        # Scrittura del fatto nel file f
        fatto = f"libro({titolo}, {autore}, '{g1}', '{g2}', '{g3}', {pagine}, {rapporto}, {liked}).\n"
        f.write(fatto)

print("File conoscenza_libri.pl generato con successo!")

# Caricamento dei file contenenti i fatti e le regole

prolog = Prolog()
prolog.consult("conoscenza_libri.pl")
prolog.consult("regole.pl")

# Esecuzione di una query: ricerca di tutti i titoli 'consigliati'
results = list(prolog.query("consigliato(Titolo)"))

# Stampa dei risultati
print("\nLIBRI CONSIGLIATI DALLA LOGICA PROPOSIZIONALE")
for res in results:
    print(f"- {res['Titolo']}")

#Test del modello

def predict_prolog(row):
    #Pulizia della stringa titolo
    titolo_pulito = "".join(e for e in str(row['title']).lower() if e.isalnum() or e == '_')

    #Si interroga il Prolog: il libro è 'consigliato'?
    query = f"consigliato('{titolo_pulito}')"
    result = list(prolog.query(query))

    # Se la query restituisce almeno un risultato, Prolog dice 'yes'
    return 'yes' if len(result) > 0 else 'no'

# Si applica la funzione al test set
