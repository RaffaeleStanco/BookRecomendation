"""# Preparazione del dataset"""

#Importazione del dataset dal csv

df = pd.read_csv("/content/google_books_1299.csv")

#Visualizzazione delle colonne del DataFrame e delle informazioni sui loro tipi

print(df.columns)
df.info()

#Eliminazione delle colonne considerate superflue per le fasi successive

df.drop(['Unnamed: 0', 'price', 'currency', 'language', 'published_date'],axis=1, inplace=True)
#language è considerata superflua perchè il dataset contiene solo libri in lingua inglese

#Rimozione dei duplicati basata sul titolo e sull'autore
df['title_clean'] = df['title'].str.lower().str.strip()
df['author_clean'] = df['author'].str.lower().str.strip()
df = df.drop_duplicates(subset=['title_clean', 'author_clean'], keep='first')
df = df.drop(columns=['title_clean', 'author_clean']).reset_index(drop=True)

print(f"Duplicati rimossi! Righe rimanenti: {len(df)}")

#Normalizzazione dei valori della colonna voters

df['voters'] = df['voters'].fillna('0') #rimuoviamo i valori nulli, sostituiti con degli 0
df['voters'] = df['voters'].str.replace(',', '') #rimuoviamo la virgola dove presente
df['voters'] = df['voters'].astype(int) #conversione in numeri interi

#Normalizzazione dei valori della colonna rating

df['rating'] = df['rating'].fillna(0)

#Aggiunta di una colonna contenente il rapporto fra il numero di votanti e la valutazione
#media del libro e normalizzazione del risultato sostituendo i valori nulli con uno zero

df['voters/rating'] = df['voters'] / df['rating']
df['voters/rating'] = np.ceil(df['voters/rating'].fillna(0))

#Eliminazione delle colonne rating e voters che non servono più

df.drop(['rating', 'voters'],axis=1, inplace=True)

#Normalizzazione del valore del genere per i libri per i quali non è specificato alcun genere

df['generes'] = df['generes'].str.replace('none', 'Unknown')
df['generes'] = df['generes'].fillna('Unknown')

#Assumendo che un libro possa avere 3 generi al massimo e che i generi siano forniti in ordine
#di rilevanza, si creano 3 nuove colonne, una per ogni genere, per poterli trattare come feature
#separate. Si elimina poi la vecchia colonna generes

generi_split = df['generes'].str.split(',', expand=True)
df['genere_1'] = generi_split[0].str.strip().fillna('Unknown')
if 1 in generi_split.columns:
    df['genere_2'] = generi_split[1].str.strip().fillna('Unknown')
else:
    df['genere_2'] = 'Unknown'
if 2 in generi_split.columns:
    df['genere_3'] = generi_split[2].str.strip().fillna('Unknown')
else:
    df['genere_3'] = 'Unknown'
df=df.drop('generes', axis=1)

#Categorizzazione del numero di pagine (Corto, Medio, Lungo)
df['page_count'] = pd.cut(df['page_count'], bins=[0, 200, 500, 2000], labels=['short', 'medium', 'long'])
df['page_count'] = df['page_count'].cat.add_categories('Unknown').fillna('Unknown')

#Categorizzazione del rating
df['voters/rating'] = pd.qcut(df['voters/rating'], q=4, labels=['low', 'average', 'high', 'top'])

#Aggiunta al DataFrame di una colonna in cui segnare se all'utente piace o meno il libro

df['liked'] = ''

#Scelta dei generi di interesse da usare come profilo dei gusti di un utente fittizio,
#così da generare in maniera realistica i dati per compilare la colonna liked

gusti = ['fiction', 'fantasy', 'mystery &amp', 'graphic novels']

#Generazione dei valori per compilare la colonna liked

gusti_set=set(gusti)
for id, row in df.iterrows():
    generi_libro = [str(row['genere_1']).lower(),
                    str(row['genere_2']).lower(),
                    str(row['genere_3']).lower()]
    match = not gusti_set.isdisjoint(generi_libro)
    if match:
      prob=(0.9, 0.1)
    else:
      prob=(0.1, 0.9)
    df.loc[id, 'liked'] = np.random.choice(['yes', 'no'], p=prob)

#caricamento di un dataset precedente ai fini di mostrare risultati stabili.
#Le precedenti celle sono presenti per mostrare le procedure di feature engeneering effettuate

df = pd.read_csv("dataset_books.csv")

#Divisione del DataFrame in Train e Test set

train, test = train_test_split(df, test_size=0.2, random_state=42, stratify=df['liked'])

"""# Albero Decisionale"""

#Selezione delle features utili all'albero e creazione dei Train e Test set

features = ['author', 'publisher', 'page_count', 'genere_1', 'genere_2', 'genere_3', 'voters/rating']
X_train= train[features]
y_train = train['liked']
X_test= test[features]
y_test = test['liked']

#Creazione della pipeline per eseguire l'encoding e la classificazione implementando SMOTE
pipeline_smote = ImbPipeline(steps=[('encoder', OrdinalEncoder(handle_unknown='use_encoded_value', unknown_value=-1)),
                                    ('smote', SMOTE(random_state=42)),
                                    ('classifier', DecisionTreeClassifier(random_state=42))])

#Ricerca dei parametri migliori per l'albero tramite una gridsearch

#Definizione dei parametri da esplorare
param_grid = {
    'classifier__max_depth': [3, 5, 7, 10, None],
    'classifier__min_samples_split': [2, 5, 10],
    'classifier__criterion': ['gini', 'entropy'],
    'smote__k_neighbors': [3, 5]}

#Creazione del ricercatore
grid_smote = GridSearchCV(pipeline_smote, param_grid, cv=5, scoring='f1_weighted')

#Avvio dell'addestramento
grid_smote.fit(X_train, y_train)

#Stampa dei migliori risultati
print(f"Migliori parametri trovati (con SMOTE): {grid_smote.best_params_}")

#Prelievo del modello migliore
y_pred_best = grid_smote.best_estimator_.predict(X_test)

#Stampa del report di classificazione
